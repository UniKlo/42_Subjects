int	is_anagram(char *a, char *b)
{
	int alpha[127];
	int i = 0;

	while (i < 127)
		alpha[i++] = 0;
	i = 32;
	
	while (*a)
	{
		alpha[(int)(a[0])]++;
		a++;
	}
	while (*b)
	{
		alpha[(int)(b[0])]--;
		b++;
	}
	while (i < 127)
	{
		if (alpha[i] != 0)
			return (0);
		i++;
	}
	return (1);
}
